/**
 * Returns the sum of the second argument and half of the first argument.
 *
 * @param a - The first number. Its half will be added to the second number.
 * @param b - The second number.
 * @returns The result of b + a/2.
 */
export function halfAdd(a: number, b: number): number {
  return b + a / 2;
}
